package com.stillvalid.asus.stillvalid.Models;

public class Papiers {
    private int id;
    private String path;
    private String id_contrat;

    public Papiers() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getId_contrat() {
        return id_contrat;
    }

    public void setId_contrat(String id_user) {
        this.id_contrat = id_user;
    }
}
